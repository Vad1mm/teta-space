/**
 * Get a random brain fact for the current locale.
 * @param {string} locale - 'uk' or 'en'
 * @param {number} [seed] - optional seed for deterministic selection
 * @returns {string}
 */
export function getRandomFact(locale, seed) {
  const facts = brainFacts[locale] || brainFacts.en;
  const idx = seed != null
    ? Math.abs(seed) % facts.length
    : Math.floor(Math.random() * facts.length);
  return facts[idx];
}
